let a = 5;
console.log(a == 5, 'igualdad'); //true
console.log(a != 5, 'desigualdad'); //false
console.log(a === '5', 'igualdad estricta'); //false
console.log(a !== '5', 'desigualdad estricta'); //true
console.log(a > 5, 'mayor que'); //false
console.log(a < 5, 'menor que'); //false
console.log(a >= 5, 'mayor o igual que'); //true
console.log(a <= 5, 'menor o igual que'); //true
console.log(a > 5 && a < 10, 'and'); //false
console.log(a > 5 || a < 10, 'or'); //true
console.log(!true, 'not'); //false
console.log(!false, 'not'); //true
